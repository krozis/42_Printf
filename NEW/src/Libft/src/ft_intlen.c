#include "libft.h"

size_t	ft_intlen(int nb)
{
	size_t	len;

	if (nb > -10 && nb < 10)
		return (1);
	len = 0;
	while (nb != 0)
	{
		nb = nb / 10;
		len++;
	}
	return (len);
}
